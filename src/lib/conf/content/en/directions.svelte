<p>
  <strong>By Car:</strong>
</p>
<ol> 
  <li>Take the A11 highway from Berlin to Stettin (PL).</li>
  <li>From there take the A20 to Greifswald.</li>
  <li>Take the B105 towards Usedom and then turn onto the L20 towards Koserow.</li>
  <li>From Hauptstraße turn right into Fischerstraße and at the next intersection turn right again into Achterstraße.</li>
  <li>Your destination is on the right.</li>
</ol>
<p>
<strong>By Train:</strong> 
</p>
<ol>
  <li>Take a train from Berlin to Greifswald.</li>
  <li>From Greifswald, take the UBB-line train towards Swinemünde(PL) and get off at the Koserow station.</li>
  <li>From the station, you can take a taxi or walk to your destination.</li>
  <li>We can arrange a pickup if you have a lot of luggage.</li>
</ol>

