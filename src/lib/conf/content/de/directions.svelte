<p>
  <strong>Mit dem Auto:</strong>
</p>
<ol>
  <li>Nehmen Sie die Autobahn A11 von Berlin nach Stettin (PL).</li>
  <li>Von dort nehmen Sie die A20 nach Greifswald.</li>
  <li>Nehmen Sie die B105 in Richtung Usedom und biegen Sie dann auf die L20 in Richtung Koserow ab.</li>
  <li>Von der Hauptstraße biegen Sie rechts in die Fischerstraße ein und an der nächsten Kreuzung wieder rechts in die Achterstraße.</li>
  <li>Ihr Ziel liegt rechts.</li>
</ol>
<p>
  <strong>Mit der Bahn:</strong>
</p>
<ol>
  <li>Nehmen Sie einen Zug von Berlin nach Greifswald.</li>
  <li>Von Greifswald aus nehmen Sie den UBB-Linienzug in Richtung Swinemünde (PL) und steigen an der Koserower Station aus.</li>
  <li>Vom Bahnhof aus können Sie ein Taxi oder zu Fuß zu Ihrem Ziel nehmen.</li>
  <li>Ggf. können wir eine Abholung vom Zug arrangieren, wenn Sie sehr viel Gepäck haben.</li>
</ol>
  